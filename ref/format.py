def board_print(board):
    for i in [4, 3, 2, 1, 0]:
        print(i, ":", end=" ")
        for j in range(5):
            print(convert(board[i][j]), end=" ")
        print()
    print("   ", 0, 1, 2, 3, 4)
    print("")


def convert(x):
    return 'b' if x == 1 else 'r' if x == -1 else '.'




